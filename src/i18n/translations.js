function getNestedValue(obj, path) {
  return String(path || "")
    .split(".")
    .reduce((acc, key) => (acc && acc[key] !== undefined ? acc[key] : undefined), obj);
}

export function getLanguageMeta(code = "en") {
  return SUPPORTED_LANGUAGES.find((item) => item.code === code) || SUPPORTED_LANGUAGES[0];
}

export function t(language = "en", key = "", fallback = "") {
  const dict = TRANSLATIONS[language] || TRANSLATIONS.en;
  return getNestedValue(dict, key) ?? getNestedValue(TRANSLATIONS.en, key) ?? fallback ?? key;
}